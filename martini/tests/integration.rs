pub(crate) fn assert_float_array(a: &[f32], b: &[f32]) {
    for (i, v) in a.iter().enumerate() {
        if v != &b[i] {
            panic!("{} != {}", v, b[i]);
        }
    }
}

pub(crate) fn assert_unsigned_array(a: &[u32], b: &[u32]) {
    for (i, v) in a.iter().enumerate() {
        if v != &b[i] {
            panic!("{} != {}", v, b[i]);
        }
    }
}
